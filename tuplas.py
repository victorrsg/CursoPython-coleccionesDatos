tupla=(100,"Hola",4,4,123,4)

 
print(tupla[-1])          # Último elemento
print(len(tupla))         # Número de elementos que tiene
print(tupla.index(123))   # Posición donde se encuentra el número 123
print(tupla[:3])          # Sublista con los primeros tres elementos
print(tupla[4])           # Elemento que hay en la posición con índice 4 
print(tupla.count(4))     # Número de veces que aparece el número 4